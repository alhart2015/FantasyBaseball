def classify(name, closers, pitchers):
    if name in closers:
        return "CL"
    if name in pitchers:
        return "SP"
    if "(Batter)" in name or "(Pitcher)" in name:
        return "H" if "(Batter)" in name else "SP"
    return "H"


def parse_draft(text, closers, pitchers):
    """Parse a draft from copy-paste format."""
    picks = []
    current_round = 0
    for line in text.strip().split("\n"):
        line = line.strip()
        if not line:
            continue
        if line.startswith("Round "):
            current_round = int(line.split()[1])
            continue
        # Parse: "1.    Player Name    Team Name"
        parts = line.split("    ")
        parts = [p.strip() for p in parts if p.strip()]
        if len(parts) >= 3:
            pick_num = parts[0].rstrip(".")
            player = parts[1]
            team = parts[2]
            # Expand truncated team names
            team_map = {
                "Hart of the ...": "Hart of the Order",
                "Work in Prog...": "Work in Progress",
                "Tortured Bas...": "Tortured Baseball Department",
                "Boston Estre...": "Boston Estrellas",
                "Springfield ...": "Springfield Isotopes",
                "Crews Control": "Crews Control",
                "Jon's Underdogs": "Jon's Underdogs",
            }
            team = team_map.get(team, team)
            ptype = classify(player, closers, pitchers)
            picks.append({
                "round": current_round,
                "pick": int(pick_num),
                "player": player,
                "team": team,
                "type": ptype,
            })
    return picks


