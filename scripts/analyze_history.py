def analyze_team(picks, team_name):
    """Analyze a team's draft tendencies."""
    team_picks = [p for p in picks if p["team"] == team_name]
    sp_rounds = [p["round"] for p in team_picks if p["type"] == "SP"]
    cl_rounds = [p["round"] for p in team_picks if p["type"] == "CL"]
    h_rounds = [p["round"] for p in team_picks if p["type"] == "H"]

    first_sp = min(sp_rounds) if sp_rounds else None
    first_cl = min(cl_rounds) if cl_rounds else None
    early = [p for p in team_picks if p["round"] <= 5]
    early_h = sum(1 for p in early if p["type"] == "H")
    early_p = sum(1 for p in early if p["type"] in ("SP", "CL"))

    return {
        "total": len(team_picks),
        "hitters": len(h_rounds),
        "sp": len(sp_rounds),
        "closers": len(cl_rounds),
        "first_sp": first_sp,
        "first_cl": first_cl,
        "early_h": early_h,
        "early_p": early_p,
        "cl_rounds": cl_rounds,
        "sp_rounds": sp_rounds,
    }


def main():
    picks_2025 = parse_draft(DRAFT_2025, CLOSERS_2025, PITCHERS_2025)
    picks_2024 = parse_draft(DRAFT_2024, CLOSERS_2024, PITCHERS_2024)

    all_teams = set(p["team"] for p in picks_2025) | set(p["team"] for p in picks_2024)
    # Only analyze teams present in both years
    common = set(p["team"] for p in picks_2025) & set(p["team"] for p in picks_2024)

    print("=" * 90)
    print("OPPONENT DRAFT PROFILES (2024-2025)")
    print("=" * 90)

    for team in sorted(common):
        a25 = analyze_team(picks_2025, team)
